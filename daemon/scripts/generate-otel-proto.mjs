import { execFile } from "node:child_process";
import { mkdtemp, readFile, rm, writeFile } from "node:fs/promises";
import { tmpdir } from "node:os";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { promisify } from "node:util";

const exec = promisify(execFile);
const daemon = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const output = join(daemon, "src/otel-proto-schema.ts");
const entrypoint = "opentelemetry/proto/collector/trace/v1/trace_service.proto";
const temp = await mkdtemp(join(tmpdir(), "orchestra-otel-proto-"));
try {
  const json = join(temp, "descriptor.json");
  await exec(join(daemon, "node_modules/.bin/pbjs"), ["-t", "json", "-p", join(daemon, "otel-proto"), "-o", json, entrypoint], { cwd: join(daemon, "otel-proto") });
  const descriptor = JSON.parse(await readFile(json, "utf8"));
  const normalize = value => Array.isArray(value) ? value.map(normalize)
    : value && typeof value === "object" ? Object.fromEntries(Object.keys(value).sort().map(key => [key, normalize(value[key])]))
    : value;
  const generated = `// Generated by scripts/generate-otel-proto.mjs. Do not edit.\nexport const otelProtoSchema = ${JSON.stringify(normalize(descriptor), null, 2)} as const;\n`;
  if (process.argv.includes("--check")) {
    const current = await readFile(output, "utf8").catch(() => "");
    if (current !== generated) throw new Error("src/otel-proto-schema.ts is stale; run pnpm generate:otel-proto");
  } else await writeFile(output, generated);
} finally {
  await rm(temp, { recursive: true, force: true });
}
